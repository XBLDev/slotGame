function ButtonTransforms()
{
    this.SpinBtnWidth = window.innerWidth/4;
    this.SpinBtnX = window.innerWidth - this.SpinBtnWidth;//window.innerWidth, window.innerHeight
    this.SpinBtnHeight = window.innerHeight/5;
    this.SpinBtnY = window.innerHeight - this.SpinBtnHeight;//window.innerWidth, window.innerHeight

    this.upBtnwidth = window.innerWidth/4 - window.innerWidth/5;
    this.upBtnheight = window.innerHeight/10;
    this.upBtnX =  window.innerWidth/5;//window.innerWidth/2
    this.upBtnY =  window.innerHeight - 2 * window.innerHeight/10;

    this.downBtnwidth = window.innerWidth/4 - window.innerWidth/5;
    this.downBtnheight = window.innerHeight/10;
    this.downBtnX =  window.innerWidth/5;
    this.downBtnY =  window.innerHeight - window.innerHeight/10;





}